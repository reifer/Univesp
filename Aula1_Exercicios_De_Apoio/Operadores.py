# Define as variáveis a, b e c com valores inteiros e de ponto flutuante
a = 10    # Variável inteira
b = 3     # Variável inteira
c = 10.5  # Variável de ponto flutuante

# Imprime os resultados das operações
print("a / b = ", a/b)     # Imprime o resultado da divisão de a por b
print("a // b = ", a//b)   # Imprime o resultado da divisão inteira de a por b
print("a % b = ", a % b)   # Imprime o resto da divisão de a por b
print("c // b = ", c / b)  # Imprime o resultado da divisão de c por b
print("c % b = ", c % b)   # Imprime o resultado da divisão inteira de c por b