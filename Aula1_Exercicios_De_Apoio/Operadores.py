# Define as variáveis a, b e c com valores inteiros e de ponto flutuante
a = 10  # Variável inteira
b = 3   # Variável inteira
c = 10.5  # Variável de ponto flutuante

# Realiza operações aritméticas com as variáveis
r1 = a / b  # Divisão de a por b (resultado em ponto flutuante)
r2 = a // b  # Divisão inteira de a por b (resultado inteiro)
r3 = a % b  # Resto da divisão de a por b
r4 = c / b  # Divisão de c por 3 (resultado em ponto flutuante)

# Imprime os resultados das operações
print("a / b = ", r1)  # Imprime o resultado da divisão de a por b
print("a // b = ", r2)  # Imprime o resultado da divisão inteira de a por b
print("a % b = ", r3)  # Imprime o resto da divisão de a por b
print("c % b = ", r4)  # Imprime o resultado da divisão de c por b